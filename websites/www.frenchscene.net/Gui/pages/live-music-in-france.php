  <h1>Live Music in France</h1> <br />
  <br />
  If you like listening to French music, then you'e sure to love listening to it played live - and where better to do so than in France itself? If you're unfamiliar with the live music scene in France, then here are just a few of the venues to which you might consider paying a visit. <br />
  <br />
  Wayne's <br />
  <br />
  Wayne's bar is located in Nice, and boasts live music every single night. Although the evenings showing international sports matches can get busy, head down on a Thursday for a chilled out atmosphere, and love acoustic music. If you're looking to dance the night away to classic rock and pop tunes, then this is the place for you. <br />
  <br />
  Le Carmen <br />
  <br />
  If you're looking for something a little more upmarket, then Le Carmen us certainly well worth a visit. If you've spent the day sat in an office chair, looking at spreadsheets or playing <a href="http://www.cheekybingo.com/" target="_blank">CheekyBingo</a>, then this live music venue can offer an indulgent and luxurious alternative come the evening. You'll hear music from some of the trendiest musicians in Paris and around the rest of the world - so what are you waiting for? <br />
  <br />
  Thelonius <br />
  <br />
  If you're keen to catch up-and-coming talent, then head to Bordeaux's Thelonius bar. Perfect for jazz fans, this atmospheric venue hosts performances from some of the hottest solo musicians in town. If you want to be the first to see the next big thing in the world of jazz, then this is the place to be. <br />
  <br />
  Autour de Midi <br />
  <br />
Hidden behind the better known Moulin Rouge venue, the Autour de Midi is a cellar-style venue, featuring everything from jazz, to swing, bebop tunes. If you're a looking for a laid-back alternative to many of Paris' more high-profile jazz clubs, then this is certainly something of a hidden treasure. </div>



